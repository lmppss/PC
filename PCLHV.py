import streamlit as st
import numpy as np
import pandas as pd
import joblib
import datetime
import pytz
import plotly.graph_objects as go
import os
from io import BytesIO

# Cargar el modelo
modelo = joblib.load("PC_0.8722_12.04.pkl")

# Ruta para historial
historial_path = "historial_predicciones.csv"
if not os.path.exists(historial_path):
    pd.DataFrame(columns=["FechaHora", "Analista", "Cenizas", "PC", "PC real"]).to_csv(historial_path, index=False)

# T√≠tulo
st.title("üî• Predicci√≥n del Poder Calor√≠fico del Carb√≥n")
st.markdown("Ingrese los datos manualmente o pegue una fila completa separada por **coma, espacio o tabulaci√≥n**.")

# Entrada r√°pida
st.subheader("üìã Entrada r√°pida (una l√≠nea completa)")
entrada_linea = st.text_input("Pegue aqu√≠ una fila completa con los 11 valores en orden:")

# Inicializa el estado de entrada manual
if "mostrar_manual" not in st.session_state:
    st.session_state.mostrar_manual = False

if st.button("üìù Mostrar entrada manual"):
    st.session_state.mostrar_manual = not st.session_state.mostrar_manual

# Lista de analistas
analistas = sorted(["Giomara C.", "Walter G.", "Julio O.", "Jhony V.", "Kenyi A."]) + ["Otros"]
analista = st.selectbox("üë§ Seleccione el analista que realiza la predicci√≥n:", analistas)

# Entrada manual
if st.session_state.mostrar_manual:
    cenizas_bs = st.number_input("Cenizas (BS) (%)", min_value=0.0)
    sio2 = st.number_input("SiO2 ash (%)", min_value=0.0)
    al2o3 = st.number_input("Al2O3 ash (%)", min_value=0.0)
    fe2o3 = st.number_input("Fe2O3 ash (%)", min_value=0.0)
    cao = st.number_input("CaO ash (%)", min_value=0.0)
    mgo = st.number_input("MgO ash (%)", min_value=0.0)
    so3 = st.number_input("SO3 ash (%)", min_value=0.0)
    na2o = st.number_input("Na2O ash (%)", min_value=0.0)
    k2o = st.number_input("K2O ash (%)", min_value=0.0)
    s_carbon = st.number_input("S carb√≥n (%)", min_value=0.0)
    cl_carbon = st.number_input("Cl carb√≥n (%)", min_value=0.0)

# Validaci√≥n
def validar_entrada(entrada):
    entrada = entrada.replace(",", ".")
    if entrada == "":
        return False
    try:
        valores = list(map(float, entrada.strip().split()))
        return len(valores) == 11
    except ValueError:
        return False

# Bot√≥n de predicci√≥n
if st.button("üîÆ Predecir Poder Calor√≠fico"):
    if entrada_linea:
        if not validar_entrada(entrada_linea):
            st.error("‚ö†Ô∏è Formato incorrecto. Ingrese exactamente 11 valores num√©ricos.")
            st.stop()

        sep = "," if "," in entrada_linea else "\t" if "\t" in entrada_linea else " "
        valores = list(map(float, entrada_linea.strip().split(sep)))
    else:
        valores = [cenizas_bs, sio2, al2o3, fe2o3, cao, mgo, so3, na2o, k2o, s_carbon, cl_carbon]

    valores_np = np.array(valores).reshape(1, -1)
    pc_predicho = modelo.predict(valores_np)[0]
    pc_entero = int(round(pc_predicho))

    st.success(f"üî• Poder Calor√≠fico Predicho: **{pc_entero} kcal/kg**")

    ahora_lima = datetime.datetime.now(pytz.timezone('America/Lima'))
    nuevo = pd.DataFrame([{
        "FechaHora": ahora_lima.strftime('%Y-%m-%d %H:%M:%S'),
        "Cenizas": valores[0],
        "PC": pc_entero,
        "PC real": None,
        "Analista": analista
    }])

    # Cargar historial
    historial = pd.read_csv(historial_path)

    # Asegurar que columnas necesarias existen
    if "PC real" not in historial.columns:
        historial["PC real"] = None

    # Concatenar nueva fila y guardar
    historial = pd.concat([historial, nuevo], ignore_index=True)
    historial.to_csv(historial_path, index=False)

# Leer historial
historial = pd.read_csv(historial_path)

# Asegurar columnas
if "PC real" not in historial.columns:
    historial["PC real"] = None

# Calcular diferencia si hay PC real
historial["Diferencia"] = np.where(
    pd.to_numeric(historial["PC real"], errors='coerce').notna(),
    pd.to_numeric(historial["PC real"], errors='coerce') - historial["PC"],
    np.nan
)

# NUEVO BLOQUE: Secci√≥n colapsable para ingresar PC real
st.subheader("üìù Ingresar PC real manualmente")

if "mostrar_pc_real" not in st.session_state:
    st.session_state.mostrar_pc_real = False

if st.button("üóÇÔ∏è Mostrar/Ocultar ingreso de PC real"):
    st.session_state.mostrar_pc_real = not st.session_state.mostrar_pc_real

if st.session_state.mostrar_pc_real:
    fechas_disponibles = historial[historial["PC real"].isna()]["FechaHora"].tolist()
    if fechas_disponibles:
        fecha_seleccionada = st.selectbox("Seleccione la fecha de la predicci√≥n:", fechas_disponibles, key="select_fecha")
        pc_real_input = st.number_input("Ingrese el PC real para esta fecha:", min_value=0, key="input_pc_real")
        if st.button("üì• Cargar PC real"):
            if pc_real_input > 0:
                historial.loc[historial["FechaHora"] == fecha_seleccionada, "PC real"] = pc_real_input

                # Recalcular diferencia
                historial["Diferencia"] = np.where(
                    pd.to_numeric(historial["PC real"], errors='coerce').notna(),
                    pd.to_numeric(historial["PC real"], errors='coerce') - historial["PC"],
                    np.nan
                )

                diferencia_actual = historial.loc[historial["FechaHora"] == fecha_seleccionada, "Diferencia"].values[0]
                if abs(diferencia_actual) > 150:
                    st.error(f"‚ö†Ô∏è Alerta: La diferencia entre el PC real y el predicho es de {diferencia_actual:.1f} kcal/kg, mayor al umbral de 150.")
                else:
                    st.success(f"‚úÖ PC real de {fecha_seleccionada} actualizado a {pc_real_input} kcal/kg.")

                historial.to_csv(historial_path, index=False)
            else:
                st.warning("‚ö†Ô∏è Ingrese un valor v√°lido para el PC real.")
    else:
        st.info("üéâ No hay predicciones pendientes para actualizar PC real.")


# Filtro por analista
st.subheader("üîç Filtrar por analista")
analistas_disponibles = historial["Analista"].unique().tolist()
analistas_seleccionados = st.multiselect(
    "Seleccione uno o varios analistas:",
    options=analistas_disponibles,
    default=analistas_disponibles,
    placeholder="Seleccione uno o varios analistas..."
)

# Aplicar filtro
historial = historial[historial["Analista"].isin(analistas_seleccionados)].copy()
historial["FechaHora"] = pd.to_datetime(historial["FechaHora"], errors='coerce')
historial = historial.sort_values("FechaHora").tail(20)
# Gr√°fico
if not historial.empty:
    st.subheader("üìà Historial de Predicciones (filtrado)")

    fig = go.Figure()

    fig.add_trace(go.Scatter(
        x=historial["FechaHora"],
        y=historial["PC"],
        mode="lines",
        name="Tendencia PC",
        line=dict(color="orange", width=2)
    ))

    fig.add_trace(go.Scatter(
        x=historial["FechaHora"],
        y=historial["PC"],
        mode="markers",
        name="Predicciones",
        marker=dict(
            size=historial["Cenizas"] * 2,
            color=historial["PC"],
            colorscale="YlOrRd",
            colorbar=dict(title="PC (kcal/kg)", len=0.75),
            showscale=True,
            line=dict(width=0.5, color='white')
        ),
        text=[
            f"Analista: {a}<br>PC: {pc:.0f} kcal/kg<br>Cenizas: {cen:.2f}%"
            for a, pc, cen in zip(historial["Analista"], historial["PC"], historial["Cenizas"])
        ],
        hoverinfo="text"
    ))

    fig.update_layout(
        title="Poder Calor√≠fico vs Fecha",
        xaxis_title="Fecha y Hora",
        yaxis_title="Poder Calor√≠fico (kcal/kg)",
        template="plotly_dark",
        hovermode="closest",
        height=500
    )

    st.plotly_chart(fig, use_container_width=True)

    # Tabla editable
    st.subheader("üìÉ Resumen de predicciones recientes (filtradas)")

    historial_df = historial[["FechaHora", "Analista", "Cenizas", "PC", "PC real", "Diferencia"]]
    historial_df["Eliminar"] = False
    edited_df = st.data_editor(historial_df, num_rows="dynamic", use_container_width=True)

    if st.button("‚ùå Eliminar seleccionadas"):
        eliminadas = edited_df[edited_df["Eliminar"] == True]
        if not eliminadas.empty:
            historial_df = edited_df[edited_df["Eliminar"] == False].drop(columns=["Eliminar"])
            historial_df.to_csv(historial_path, index=False)
            st.success(f"Se eliminaron {len(eliminadas)} predicciones.")
            st.rerun()
        else:
            st.warning("No se seleccionaron filas para eliminar.")

    # Descargar Excel
    st.subheader("üìÖ Descargar historial completo")
    df_completo = pd.read_csv(historial_path)
    buffer = BytesIO()
    with pd.ExcelWriter(buffer, engine='xlsxwriter') as writer:
        df_completo.to_excel(writer, index=False, sheet_name='Historial')
    st.download_button(
        label="üìÑ Descargar en Excel",
        data=buffer.getvalue(),
        file_name="historial_predicciones.xlsx",
        mime="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
    )